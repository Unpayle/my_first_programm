#Описание
# / - обычное деление
test1 = 10
test2 = test1 / 5
print (test2)
# // - деление без остатка
test3 = 13
test4 = test3 // 4
print (test4) # 3 (На самом деле должно быть 3,25)
# % - убираем лишнее до целочисленного деления (остаток выводим)
test5 = 16
test6 = test5 % 5
print (test6) # (1, т.к 1 лишняя)

